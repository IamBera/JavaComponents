package day17_While_DoWhile;

public class LoopPractices {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        int j=1;
        System.out.println("---------------------------");
        while(j <= 10){
            System.out.println(j);
            j++;
        }
    }
}
